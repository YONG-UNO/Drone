static uint8_t Get_CRC8(uint8_t *pData, uint32_t len)
{
    uint8_t check = 0;
    while(len--)
    {
        check = CRC8_Table[check ^ *pData];
        pData++;
    }
    return check;
}

static uint16_t Get_CRC16(uint8_t *pData, uint32_t len)
{
    uint16_t check = 0xFFFF;
    uint32_t i;
    while(len--)
    {
        check ^= *pData++;
        for(i=0; i<8; i++)
        {
            if(check & 1) check = (check >> 1) ^ 0xA001;
            else check >>= 1;
        }
    }
    return check;
}

void ui_send_packet(uint16_t cmd_id, uint8_t *data, uint16_t data_len) {
    uint8_t buf[128] = {0};
    uint16_t total_len = 0;

    buf[0] = 0xA5;
    buf[1] = data_len & 0xFF;
    buf[2] = (data_len >> 8) & 0xFF;
    buf[3] = 0x01;
    buf[4] = Get_CRC8(buf,4);
    total_len = 5;

    buf[total_len++] = cmd_id & 0xFF;
    buf[total_len++] = (cmd_id >> 8) & 0xFF;

    for (uint16_t i = 0; i < data_len; i++) {
        buf[total_len++] = data[i];
    }

    uint16_t crc16 = Get_CRC16(buf, total_len);
    buf[total_len++] = crc16 & 0xFF;
    buf[total_len++] = (crc16 >> 8) & 0xFF;

    HAL_UART_Transmit(&huart1, buf, total_len, 100);
}
// 清屏 0x0300
void ui_clear(void)
{
    uint8_t data[2] = {0x01, 0x01};
    ui_send_packet(0x0300, data, 2);
    osDelay(20);
}

